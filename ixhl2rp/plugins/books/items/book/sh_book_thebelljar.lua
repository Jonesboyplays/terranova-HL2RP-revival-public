By nine in the morning the fake, country-wet freshness that somehow seeped in overnight evaporated like the tail end of a sweet dream. Mirage-gray at the bottom of their granite canyons, the hot streets wavered in the sun, the car tops sizzled and glittered, and the dry, cindery dust blew into my eyes and down my throat. I kept hearing about the Rosenbergs over the radio and at the office till I couldn't get them out of my mind. It was like the first time I saw a cadaver. For weeks afterward, the cadaver's head -- or what there was left of it -- floated up behind my eggs and bacon at breakfast and behind the face of Buddy Willard, who was responsible for my seeing it in the first place, and pretty soon I felt as though I were carrying that cadaver's head around with me on a string, like some black, noseless balloon stinking of vinegar. 


